package test.member.dto;

public class TodoDto {
	private int num;
	private String list;
	private String impo;
	private String due;
	
	public TodoDto () {}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getList() {
		return list;
	}

	public void setList(String list) {
		this.list = list;
	}

	public String getImpo() {
		return impo;
	}

	public void setImpo(String impo) {
		this.impo = impo;
	}

	public String getDue() {
		return due;
	}

	public void setDue(String due) {
		this.due = due;
	}
	
	
}
